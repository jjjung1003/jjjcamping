	package kr.co.jjjcamping.dto;

public class CampDto {

	private int id;
	private long m_imgsize, d_imgsize;
	private String title, address, phone, m_img, d_img, on_price, off_price, writeday;
	
	
	
	public String getOn_price() {
		return on_price;
	}
	public void setOn_price(String on_price) {
		this.on_price = on_price;
	}
	public String getOff_price() {
		return off_price;
	}
	public void setOff_price(String off_price) {
		this.off_price = off_price;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public long getM_imgsize() {
		return m_imgsize;
	}
	public void setM_imgsize(long m_imgsize) {
		this.m_imgsize = m_imgsize;
	}
	public long getD_imgsize() {
		return d_imgsize;
	}
	public void setD_imgsize(long d_imgsize) {
		this.d_imgsize = d_imgsize;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getM_img() {
		return m_img;
	}
	public void setM_img(String m_img) {
		this.m_img = m_img;
	}
	public String getD_img() {
		return d_img;
	}
	public void setD_img(String d_img) {
		this.d_img = d_img;
	}
	public String getWriteday() {
		return writeday;
	}
	public void setWriteday(String writeday) {
		this.writeday = writeday;
	}
	
	
	
	
	
}
